"""
tipos de dados

str - string - "hello", 'hello'...
int - inteiro - ... -1,0,1.........
float/real - ... -1.0 , 0.0 , 1.0.........
bool- booleano/lógico - testa a logica true/false

obs - todo comando que usa () em python geralmente é função ou classe
"""
print('eudes', type('eudes'))
print(7, type(7))
print(7.0, type(7.0))
print(7 == 7, type(7 == 7))

# Casting - converter um tipo em outro tipo

print('10', type(int('10')))
print(10.10, type(int(10)))

print('eudes sena', type('eudes sena'))
print(24, type(24))
print(1.67, type(1.67))
print(24 > 18, type(24 > 18))
