"""
todo comando que tiver parenteses é uma função
ex: print()
funções executam ações e podem receber argumentos

sep ='' - o conteudo entre as aspas sao o separador
end ='' - define o que é executado no fim do print ( por padrao o print quebra uma linha )
sep e end são argumentos de separação e fim da função print
python é case sensitive
"""
print('eudes', 'sena', sep='-')  # os conteudos serão separados por um traço.
print('eudes', 'sena', end=' ')  # no fim do print nao vai ser executada nada, nem a quebra de linha.

