"""
estrutura de condição ( IF,ELSE e ELIF )

expressões condicionais executam de acordo com um retorno booleano
se a condição for verdadeira executa o bloco do if ou elif
se nenhuma condição for verdadeira, executa o bloco do else
"""

if False:  # ta perguntando se a expressão é verdadeira
    print('verdadeiro')
elif True:
    print('ok')
else:
    print('falso')

#  nesse exemplo será executado "ok" pois é o unico com expressão true


